export const getUWStats = () => {
  return {
    totalData: mockUWData.length,
    totalDisplayed: mockUWData.length
  };
};

export const searchUWData = (searchTerm) => {
  if (!searchTerm) return mockUWData;
  
  const term = searchTerm.toLowerCase();
  return mockUWData.filter(item => 
    item.uw.toLowerCase().includes(term) ||
    item.code.toLowerCase().includes(term) ||
    item.companyName.toLowerCase().includes(term)
  );
};

export const formatReturn = (returnValue) => {
  if (returnValue === 0) return "0.00%";
  const percentage = (returnValue * 100).toFixed(2);
  return returnValue > 0 ? `+${percentage}%` : `${percentage}%`;
};

export const formatPrice = (price) => {
  return new Intl.NumberFormat('id-ID', {
    style: 'currency',
    currency: 'IDR',
    minimumFractionDigits: 0
  }).format(price);
};